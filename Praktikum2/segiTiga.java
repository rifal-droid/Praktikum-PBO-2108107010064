/** 
* Program berikut untuk mencoba looping for segitiga terbalik
*/
public class segiTiga {
    public static void main(String[] args) {

        for(int i = 0; i < 10; i++) //untuk menentukan tinggi segitiga
{
        for(int j = 1; j <= 10-i; j++)  
{
        System.out.print("*"); //mencetak tanda bintang
    }
      System.out.println(); //mencetak baris
    }
    
    }
}
     